# En este apartado vamos a crear una función que reciba un número entero y devuelva su cuadrado

valor = 3
def cuadrado(numero): # Definir la función con un parámetro llamado número
    return  numero ** 2 # Devolver el resultado de elevar el número al cuadrado
print("El cuadrado de 3 es:", cuadrado(valor)) # Llamar a la función cuadrado con el argumento 2 y mostrar el resultado en la consola