# Crear una función que reciba un nombre y devuelva un saludo personalizado

def saludar(nombre): # Definir la función con un parámetro llamado nombre
    return " Hola"+ nombre # Devolver un saludo personalizado concatenando "Hola" con el nombre recibido como argumento
print(saludar("Wesny")) # Llamar a la función saludar con el argumento "Wesny" y mostrar el resultado en la consola