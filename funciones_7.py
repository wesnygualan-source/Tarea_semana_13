# En este apartado vamos a crear una función que calcule el promedio de dos numeros

a = 9
b = 8.5
def promedio(a, b): # Definir la función con dos parámetros
    return (a + b) / 2 # Devolver el resultado de la suma de los dos números dividida por 2
print(f"El promedio de {a} y {b} es: {promedio(a, b)}")
